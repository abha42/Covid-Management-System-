                <h3>Vomiting</h3>
                <p>According to the Centers for Disease Control and Prevention (CDC), many people with 
COVID-19 experience gastrointestinal symptoms such as nausea, vomiting, or diarrhea — sometimes even 
before developing the respiratory tract symptoms. Vomiting and other gastrointestinal issue were the only
sign of a COVID-19 infection, or a precursor to the infection. According to research published in The 
American Journal of Gastroenterology in April, more than 50% of 204 COVID-19 patients hospitalized in 
China's Hubei province between January 18 and February 28 reported digestive symptoms like lack of 
appetite, diarrhea, vomiting, or abdominal pain. Six of those patients had no respiratory symptoms 
whatsoever. Research published in the Journal of the American Medical Association, too, identified 14 
coronavirus patients who had diarrhea and nausea before they showed any signs of fever or respiratory 
symptoms — that was equal to about 10% of the study's 138-person sample size. While experts aren't 
entirely sure why or how COVID-19 may cause vomiting or other gastrointestinal symptoms, researchers have 
a theory : Because the molecule that the coronavirus attacks in the body—angiotensin-converting enzyme 2,
or ACE2—exists not only in the lungs, but in the gastrointestinal tract as well, that may explain why some
people experience stomach issues. Another study published The American Journal of Gastroenterology 
suggests that the virus can enter the digestive system through cell surface receptors for ACE2. Receptors 
for this enzyme are 100 times more common in the gastrointestinal tract than the respiratory tract. Still,
 more research needs to be done to identify the exact mechanism. </p>
