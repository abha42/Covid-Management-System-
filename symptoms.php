                <h3>Fever</h3>
                <p>Fever is a common symptom of COVID-19, but it remains unclear exactly how common. A 
2020 review in PLOS One with data from almost 25,000 adults found that fever was the most common symptom.
It was present in 78% of cases.A study in China found that only 44% of people who stayed in the hospital 
for COVID-19 had fever upon admission. However, just under 89 % experienced fever at some point during the
hospitalization. Fever appears to be one of the most common COVID-19 symptoms, but people with this 
disease do not always experience fever, and some people develop no symptoms at all.The COVID Symptom Study
app found that around four in ten people reporting symptoms of COVID-19 have a high temperature (fever).
Fever is not necessarily a bad thing. It’s a normal response to infection, as raising your body 
temperature helps your immune system fight it off. If you’re under 65, having a temperature over 37.8°C 
is likely to be a sign of COVID-19. If you’re over 65 or very thin, your normal body temperature is likely
to be lower, so a reading over 37.4°C should be considered to be a potential symptom. Fever usually 
happens in the first week of illness and tends to go quite quickly. However, some people with long-lasting 
symptoms (long COVID) have reported having recurring fever/chills. Fever is a reasonably common symptom of
COVID-19, affecting an average of four in ten (40%) of children and adults at some point in their illness.
Importantly, this means that most people (60%) with COVID-19 will not have a fever, even though it is 
considered to be one of the three ‘classic’ symptoms of the disease, along with persistent coughing and 
loss of smell (anosmia).</p>
