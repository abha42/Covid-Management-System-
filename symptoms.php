                <h3>Dry Cough</h3>
                <p>A common symptom of COVID-19 is a dry cough, which is also known as an unproductive 
cough (a cough that doesn’t produce any phlegm or mucus). A person with a dry cough may experience a dry, 
scratchy, sore, or rough throat. There may also be a sensation of a tickle in the throat or a patch of the
throat that feels dry, even when swallowing. There may not be any relief from a dry cough, even after 
coughing. A dry cough caused by COVID-19 is typically deep and low, occurring at the bottom of the lungs.
A dry cough is a common early symptom of COVID-19. According to some estimates, 60–70% of people who 
develop COVID-19 symptoms experience a dry cough as an initial symptom. Ways to help a dry cough - <br>
1) Keep yourself well hydrated by drinking small amounts often throughout the day. <br>
2) Soothe your throat by drinking a warm drink, such as honey and lemon.<br>
3) Take small sips of liquid if you feel yourself starting to cough. <br>
4) Suck a sugary sweet if you feel yourself starting to cough.<br>
5) Try swallowing repeatedly if you have a cough and don't have a drink near you.<br>
6) Blow your nose if you have a runny nose – try not to sniff.<br>
You can also use positions to ease breathing if you cough when you go to bed.
Avoid things that make you cough. For example: smoking, smoky atmospheres, air fresheners, strong smelling
candles, strong perfumes or deodorants</p>
