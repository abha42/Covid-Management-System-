                <h3>Running Nose</h3>
                <p>A runny nose can be a symptom of COVID-19. The Centers for Disease Control and 
Prevention (CDC) note that a runny nose can be a symptom of COVID-19 in both children and adults.
There are multiple variants of SARS-CoV-2. According to the Washington State Department of Health, 
a runny nose is rarely a symptom of COVID-19 that is due to previous variants of the virus, but it can 
occur.However, according to recent evidence, a runny nose may be a main symptom of COVID-19 that results 
from the recently discovered delta variant. The delta variant accounted for 10% of COVID-19 cases in the 
United States as of June 14, 2021. Recent data from the United Kingdom Zoe COVID Symptom study suggest 
that delta variant infections manifest with a headache, followed by a sore throat, a runny nose, and a 
fever. For this reason, anyone with a runny nose should consider getting a COVID-19 test.</p>
