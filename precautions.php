                    <h3>Avoid Crowded Area</h3>
                    <p> COVID-19 is primarily spread from person-to-person via the respiratory droplets 
released when an infected person coughs, speaks or even breathes. Social distancing aims to prevent this 
spread by reducing close contact between people. The CDC reports that someone infected with COVID-19 can 
be contagious before symptoms ever even begin. To make matters worse, some people remain completely 
asymptomatic, but are still contagious. Preventive measures like social distancing help protect vulnerable
 people from developing more serious illness. Social distancing can include large-scale measures, like 
 canceling crowded group events and closing public spaces. But it also includes a conscious, individual 
 effort from each one us to practice the measures that help prevent the spread of COVID-19. One of the 
 primary benefits of social distancing, if put into action early enough during an outbreak, is the effect
  it has on the "epidemic curve" — namely, its ability to flatten it. Someone with COVID-19 can
  potentially infect up to 3 different people at the same time. If one of the first 3 people that got 
  infected stayed home after that and not go out, the number of spread would not be that big. Over time,
