                    <h3>Always Wear A Mask</h3>
                    <p>Face masks combined with other preventive measures can help slow the spread of the 
virus that causes COVID-19. The U.S. Centers for Disease Control and Prevention (CDC) recommends wearing a
mask indoors in public and outdoors in crowded areas or when you are in close contact with unvaccinated 
people. The effectiveness of masks can be improved by ensuring that the masks are well fitted to the 
contours of your face to prevent leakage of air around the masks' edges. Masks should be snug over the 
nose, mouth and chin, with no gaps. You should feel warm air coming through the front of the mask when you
breathe out. You shouldn't feel air coming out under the edges of the mask. Choose masks that have 
bendable nose strips, as these help prevent air from leaking out of the top of the mask. Masks with 
several layers offer more protection. Proper use, storage and cleaning of masks also affects how well they
protect you. Follow these steps for putting on and taking off your mask :<br>
1) Wash or sanitize your hands before and after putting on your mask.<br>
2) Place your mask over your mouth and nose and chin.<br>
3) Tie it behind your head or use ear loops. Make sure it's snug against your face.<br>
4) Don't touch your mask while wearing it.<br>
5) If you accidentally touch your mask, wash or sanitize your hands.<br>
6) If your mask becomes wet or dirty, switch to a clean one. Put the used mask in a sealable bag until you
can get rid of it or wash it.<br>
7) Remove the mask by untying it or lifting off the ear loops without touching the front of the mask or 
your face. Fold the outside corners together.<br>
8) Wash your hands immediately after removing your mask.<br> 
9) Regularly wash cloth masks in the washing machine or by hand. (They can be washed along with other 
laundry.) Dry them in the dryer or hang them outside in the sun.<br>
10) Throw away disposable masks after wearing them once.<br>
And don't forget these precautions:<br>
1) Don't put masks on anyone who has trouble breathing or is unconscious or otherwise unable to remove the
 mask without help.<br>
2) Don't put masks on children under age 2.<br>
3) Don't use face masks as a substitute for physical distancing.<br>
 </p>
